class DataType2
{
 public static void main(String args[])
{
{
  Byte b1=20;
  System.out.println("byte"+b1);
  System.out.println("byte min value"+Byte.MIN_VALUE);
  System.out.println("byte max value"+Byte.MAX_VALUE);
}
{
  Integer i=23;
  System.out.println("integer"+i);
  System.out.println("integer min value"+Integer.MIN_VALUE);
  System.out.println("integer min value"+Integer.MAX_VALUE);
}
{
  Short s=675;
  System.out.println("Short"+s);
  System.out.println("short min value"+Short.MIN_VALUE);
  System.out.println("short min value"+Short.MAX_VALUE);
}
{
  Long l=675l;
  System.out.println("Long"+l);
  System.out.println("long min value"+Long.MIN_VALUE);
  System.out.println("long min value"+Long.MAX_VALUE);
}
{
  Float f=67.5f;
  System.out.println("Float"+f);
  System.out.println("float min value"+Float.MIN_VALUE);
  System.out.println("float min value"+Float.MAX_VALUE);
}
{
  Double d=67.3;
  System.out.println("Double"+d);
  System.out.println("Double min value"+Double.MIN_VALUE);
  System.out.println("Double min value"+Double.MAX_VALUE);
}
{
  Character c='a';
  System.out.println("Character"+c);
  System.out.println("Character min value"+Character.MIN_VALUE);
  System.out.println("Character min value"+Character.MAX_VALUE);
}
}
}